//Paradigmas
//imperativo ou declarativo
/* 
o imperativo foca em como será a forma de resolução do problema e o declarativo no que deve ser feito para resolver
*/

//pilares
/*
-Herança -> O objeto filho herda propriedades e métodos do pai

-Polimorfismo -> Objetos podem herdar a mesma classe pai, mas se comportarem de forma diferente quando invocamos seus métodos

-Encapsulamento -> cada classe tem propriedades e metodos independentes do restante do código

-Abstração -> consiste em isolar um aspecto derteminado de um estado de alguma coisa relativamente complexa a fim de simplificar sua avaliação, classificação ou para permitir a comunicação do mesmo
*/

